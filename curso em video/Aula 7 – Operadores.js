/*Operadores Aritméticos:
+ (adição)
- (subtração)
* (multiplicação)
/ (divisão)
% (módulo)
** (potencia)
*/
let numero1 = 5;
let numero2 = 3;
let soma = numero1 + num2; 
let subtracao = numero1 - numero2; 
let multiplicacao = numero1 * numero2;
let divisao = numero1 / numero2; 
let modulo = numero1 % numero2; 


/*Operadores de Atribuição:
= (atribuição simples)
+= (atribuição de adição)
-= (atribuição de subtração)
*= (atribuição de multiplicação)
/= (atribuição de divisão)
*/
let numero = 10;
numero += 5; 
numero -= 3; 
numero *= 2; 
numero /= 4; 

/*Operadores de Comparação:
== (igual)
=== (igual estrito)
!= (diferente)
!== (diferente estrito)
< (menor que)
> (maior que)
<= (menor ou igual a)
>= (maior ou igual a)
*/
let num1 = 5;
let num2 = "5";
num1 == num2; 
num1 === num2; 
num1 != num2; 
num1 !== num2; 
num1 < 10; 
num1 >= 5; 


/*Operadores Lógicos:
&& (E lógico)
|| (OU lógico)
! (Negação lógica)
*/
let x = true;
let y = false;
x && y; 
x || y; 
!x; 

