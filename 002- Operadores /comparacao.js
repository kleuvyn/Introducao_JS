// Igualdade (==): Verifica se dois valores são iguais, realizando coerção de tipo, se necessário.
console.log(5 == 5); // true
console.log('5' == 5); // true (coerção de tipo)

// Igualdade Estrita (===): Verifica se dois valores são estritamente iguais, sem coerção de tipo.
console.log(5 === 5); // true
console.log('5' === 5); // false (tipos diferentes)

// Diferença (!=): Verifica se dois valores são diferentes, realizando coerção de tipo, se necessário.
console.log(5 != 3); // true
console.log('5' != 5); // false (coerção de tipo)

// Diferença Estrita (!==): Verifica se dois valores são estritamente diferentes, sem coerção de tipo.
console.log(5 !== 3); // true
console.log('5' !== 5); // true (tipos diferentes)

// Maior Que (>): Verifica se o valor à esquerda é maior que o valor à direita.
console.log(5 > 3); // true

// Maior ou Igual (>=): Verifica se o valor à esquerda é maior ou igual ao valor à direita.
console.log(5 >= 5); // true

// Menor Que (<): Verifica se o valor à esquerda é menor que o valor à direita.
console.log(5 < 3); // false

// Menor ou Igual (<=): Verifica se o valor à esquerda é menor ou igual ao valor à direita.
console.log(5 <= 5); // true
