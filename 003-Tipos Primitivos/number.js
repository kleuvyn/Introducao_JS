/*
Número Inteiro:
é um número que não tem parte decimal.
*/
const numeroInteiroPositivo = 42;
console.log(numeroInteiroPositivo);

const numeroInteiroNegativo = -7;
console.log(numeroInteiroNegativo);

/*Número Decimal Ponto Flutuante:
é um número que pode ter uma parte decimal, representada por um ponto.
*/

const numeroDecimalPositivo = 3.14;
console.log(numeroDecimalPositivo);

const numeroDecimalNegativo = -0.5;
console.log(numeroDecimalNegativo);