// AND lógico (&&): Retorna verdadeiro se ambos os operandos forem verdadeiros, caso contrário, retorna falso.

console.log(true && true); // true
console.log(true && false); // false

// OR lógico (||): Retorna verdadeiro se pelo menos um dos operandos for verdadeiro, caso contrário, retorna falso.

console.log(true || false); // true
console.log(false || false); // false

// Negação lógica (!): Inverte o valor do operando booleano. Se for verdadeiro, torna-se falso, e se for falso, torna-se verdadeiro.

console.log(!true); // false
console.log(!false); // true
